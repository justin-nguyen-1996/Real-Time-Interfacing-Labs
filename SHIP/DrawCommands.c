// Function Drawship
// Uses ShipSprites
// Takes 15 10 5
//			 12 8  4
//       3  2  1
// As Direction
// Takes ExhastToggle 0,1
// Rotation Independent
//---------------------------------------------
void DrawShip(int D){
	
	WidthX = SHIPSIDELENGTH;
	WidthY = SHIPSIDELENGTH;
	DrawX = SHIPXPOSITION;
	DrawY = SHIPYPOSITION;
	if(BeenHit>0){
		ST7735_FillRect(SHIPXPOSITION-1,SHIPYPOSITION-SHIPSIDELENGTH-1,SHIPSIDELENGTH+3,SHIPSIDELENGTH+3,0x00);
		DrawFire(DrawX,DrawY);
	  DrawFire(DrawX,DrawY-4);;
		DrawFire(DrawX+4,DrawY-4);
		DrawFire(DrawX+4,DrawY);
		BeenHit--;
	}
	else{
		switch(D){
			case 1:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].DownRight,WidthX,WidthY);
				DrawX -= 2;
				DrawY -= 6 ;
				break;
			case 2:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].Down,WidthX,WidthY);
				DrawX += 2;
				DrawY -= 8;
				break;
			case 3:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].DownLeft,WidthX,WidthY);
				DrawX += 6;
				DrawY -= 6;
				break;
			case 4:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].Right,WidthX,WidthY);
				DrawX -= 5;
				DrawY -= 2;
				break;
			case 8:
				DrawShip(OldDirection);
				break;
			case 12:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].Left,WidthX,WidthY);
				DrawX += 8;
				DrawY -= 2;
				break;
			case 5:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].UpRight,WidthX,WidthY);
				DrawX -= 2;
				DrawY += 1;
				break;
			case 10:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].Up,WidthX,WidthY);
				DrawX += 2;
				DrawY += 4;
				break;
			case 15:
				OldDirection = D;
				ST7735_DrawBitmap(DrawX,DrawY,ShipSprites[ExhaustToggle].UpLeft,WidthX,WidthY);
				DrawX += 6;
				DrawY += 1;
				break;
			default:
				break;
			}
		if(ExhaustToggle == TRUE){DrawFire(DrawX,DrawY);}
	}
}

//============================================
// Function DrawGrid
// Takes Offsets and old offsets calculated in main from velocities
// First erases old grid using previous grid positions, then draws the new Grid.
// Rotation Dependent	
	#define XREPEATMAX	10
	#define	YREPEATMAX	5
// --------------------------------------------

void DrawGrid(signed int OffX,signed int OffY,signed int OldOffX,signed int OldOffY){
		for(int XRepeat = 0; XRepeat <XREPEATMAX; XRepeat++){
			for(int YRepeat = 0;YRepeat <YREPEATMAX; YRepeat++){
				ST7735_DrawPixel((XRepeat*30 - OldOffX),(YRepeat*30 - OldOffY), 0x0);
			}
		}	
		for(int XRepeat = 0; XRepeat <XREPEATMAX; XRepeat++){
			for(int YRepeat = 0;YRepeat <YREPEATMAX; YRepeat++){
				ST7735_DrawPixel((XRepeat*30 - OffX),(YRepeat*30 - OffY), 0xFFFF);
			}
		}
	}
