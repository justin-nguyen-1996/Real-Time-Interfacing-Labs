int currentNote = C4;

//------------Tone_GetSample------------
// Returns next sample in sine wave table
// Input: none
// Output: 8-bit sample
char Tone_GetSample(void) {
	static int noteCount = 0;
	int noteRate = (freqMap[currentNote]<<8)*256/10/SAMPLERATE;
	noteCount++;
	return sineWave[((noteCount * noteRate) >> 8) & 255];
}

//------------Tone_SetFrequency------------
// Set frequency note for sampling
// Input: none
// Output: none
void Tone_SetFrequency(int note) {
	currentNote = note;
}
